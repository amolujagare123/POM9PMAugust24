package utility;

public class GetterSetter {

    int a;
    double d;
    char c;
    String str ;

    public void setA(int a)
    {
        this.a = a;
    }
    public void setD(double d) {
        this.d = d;
    }

    public void setC(char c) {
        this.c = c;
    }

    public void setStr(String str)
    {
        this.str = str;
    }


    public int getA()
    {
        return a;
    }

    public char getC() {
        return c;
    }

    public double getD() {
        return d;
    }

    public String getStr()
    {
        return str;
    }

}
